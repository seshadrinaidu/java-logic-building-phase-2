/*
Problem 1: Print a Single Star (*)

Understanding the Problem:
-> I need to print only one star (*).
-> There is no user input.
-> There are no iterations.
-> No loops are required.

Choosing the Approach:
-> Since only one character needs to be printed,
   a simple System.out.println() statement is enough.
-> Using a loop would be unnecessary for this problem.

Dry Run:

System.out.println("*");

Output:
*

Program ends.

Edge Cases:
-> No user input is involved.
-> No boundary conditions.
-> No invalid input cases.

Review Solution:
-> This is the simplest pattern printing problem.
-> No loop is required.
-> Helps understand the expected output format before learning nested loops.
*/
package Level4_Star_Number_Triangle_Patterns;

public class PrintStar {
    public static void main(String[] args){
        System.out.println("*");
    }
}
